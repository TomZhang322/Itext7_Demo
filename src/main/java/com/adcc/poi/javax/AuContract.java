public class AuContract {
    private String name;

    private String unit;

    private String reg_time;

    private float con_money;

    private String perform_style;

    private String con_type;

    private String con_start_time;

    private String con_end_time;

    private String remark;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getReg_time() {
        return reg_time;
    }

    public void setReg_time(String reg_time) {
        this.reg_time = reg_time;
    }

    public float getCon_money() {
        return con_money;
    }

    public void setCon_money(float con_money) {
        this.con_money = con_money;
    }

    public String getPerform_style() {
        return perform_style;
    }

    public void setPerform_style(String perform_style) {
        this.perform_style = perform_style;
    }

    public String getCon_type() {
        return con_type;
    }

    public void setCon_type(String con_type) {
        this.con_type = con_type;
    }

    public String getCon_start_time() {
        return con_start_time;
    }

    public void setCon_start_time(String con_start_time) {
        this.con_start_time = con_start_time;
    }

    public String getCon_end_time() {
        return con_end_time;
    }

    public void setCon_end_time(String con_end_time) {
        this.con_end_time = con_end_time;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
